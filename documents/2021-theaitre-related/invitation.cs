Divadlo je scénou hl. m. Prahy a je podporováno městskou částí Praha 5
Projekt je spolufinancován se státní podporou Technologické agentury ČR v rámci Programu ÉTA 3.
Švandovo divadlo při příležitosti stoletého výročí premiéry hry R.U.R. Karla Čapka uvádí prezentaci unikátního projektu, který zkoumá, zda umělá inteligence dokáže napsat divadelní hru.
Projekt probíhá ve spolupráci Švandova divadla, MFF UK a DAMU.
