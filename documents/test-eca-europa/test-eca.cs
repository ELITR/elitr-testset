Auditní tým
Účetní dvůr ve svých zvláštních zprávách informuje o výsledcích auditů politik
a programů EU či témat z oblasti správy a řízení zaměřených na konkrétní oblasti
rozpočtu. Účetní dvůr vybírá a koncipuje tyto auditní úkoly tak, aby byl jejich dopad co
nejvyšší, a zohledňuje přitom rizika pro výkonnost a zajištění souladu s předpisy, objem
příslušných příjmů či výdajů, očekávaný vývoj, politické zájmy a zájem veřejnosti.
