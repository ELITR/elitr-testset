různé míry rozšíření v zemích EU-28 a dále se v něm zdůrazňovalo, že je třeba
přehodnotit komunikační a marketingovou strategii.
Komise se sice snažila oslovit možné příjemce, avšak neměla strukturovanou
marketingovou a komunikační strategii. Neuplatňovala dostatečně cílený přístup, aby
mezi inovačními MSP zvýšila povědomí o možnostech financování, které nástroj pro
MSP nabízí.
