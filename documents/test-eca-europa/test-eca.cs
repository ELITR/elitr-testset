Závěry a doporučení
Zjistili jsme, že nástroj pro MSP tyto podniky účinně podporuje při rozvoji jejich
inovačních projektů a že značka „EU“ pomáhá společnostem přilákat další investice.
Nebyly však vytvořeny vazby s finančními nástroji podporovanými EU, které by mohly
příjemcům pomoci expandovat a uvést inovační projekty na trh. Jak fáze 1, tak fáze 2
nástroje poskytuje MSP účinnou podporu, ačkoli s fází 1 jsou spojeny neúměrně vysoké
správní náklady. Komise řídí nástroj kompetentním způsobem. Vysoký počet
opakovaně předkládaných návrhů a omezené zdroje ztěžovaly výběrové řízení a rozvoj
služeb napomáhajících zakládání a rozvoji podniků.
Všechna doporučení platí i pro nástroj, který v rámci programu Horizont Evropa
nahradí nástroj pro MSP.
Zaměření na správné příjemce
Obecné cíle nástroje spolu se změnami uskutečněnými během jeho provádění
vyvolaly u zainteresovaných subjektů nejistotu. Současný profil odpovídá
akademickému modelu firem s vysokým růstovým potenciálem, ale z nástroje se
financují některé MSP, které mohl financovat trh (viz body 28–39).
Zeměpisný rozsah
Účast v nástroji se mezi zeměmi zřetelně liší, což částečně způsobují činitele
mimo kontrolu Komise, ale také rozdílná úroveň podpory, kterou poskytují národní
kontaktní místa a Enterprise Europe Network (viz bod 43 a 45 a obrázek 5). Komise
pořádala akce a prováděla komunikační činnosti s omezeným rozpočtem, avšak její
marketing a komunikace nebyly ani strukturované, ani dostatečně dobře zacílené, aby
oslovily správné společnosti (viz body 40–58).
Doporučení 1 – Zlepšit komunikační strategii a podporu
národních kontaktních míst, zejména v členských státech
s nejnižší mírou účasti
Komise by měla:
lépe se zaměřit na marketingovou a komunikační strategii, aby se zvýšilo
povědomí MSP o možnostech financování, které současný nástroj a budoucí
nástroj v rámci programu Horizont Evropa nabízejí;
zlepšit podporu, kterou poskytuje národním kontaktním místům pro MSP a síti
Enterprise Europe Network, tím, že bude podporovat projekty vzájemného učení
a výměnu osvědčených postupů a zajistí, aby na začátku příštího rámcového
programu začala fungovat podpora sítě národních kontaktních míst pro MSP.
Termín pro splnění doporučení: 2021
Výběr projektů
Během fungování nástroje byly zlepšeny postupy pro hodnocení a výběr.
Obzvláště užitečnou novinkou je prezentace před porotou, protože pomáhá určit
nejlepší návrhy a současně splňuje lhůtu pro udělení grantu (viz body 65–69).
Body udělované ve vzdáleném hodnocení se liší, což lze částečně vysvětlit
omezenými zdroji a vysokým počtem předložených návrhů. Školení hodnotitelů je
dobře přijímáno a současně je možno na různých úrovních poskytovat ještě více zpětné
vazby. Nástroje IT nevyhovují svému účelu, a ohrožují tak proces hodnocení
(viz body 61–64 a 70–73).
Opětovné předkládání neúspěšných návrhů výrazně a rostoucí měrou zatěžuje
zdroje určené na řízení a hodnocení a zvyšuje správní náklady. Navíc snižuje míru
úspěšnosti, a odrazuje tak účastníky (viz body 74–79).
Doporučení 2 – Zlepšit výběrové řízení
Aby se co nejlépe využívaly zdroje a efektivně se vybíraly nejlepší návrhy, měla by
Komise výběrové řízení vylepšit následujícími způsoby:
poskytnout vzdáleným hodnotitelům čas navíc na provedení jejich práce;
vytvořit obousměrný informační kanál mezi vzdálenými hodnotiteli a členy poroty,
aby členové poroty měli přístup k vzdáleným hodnocením a poskytovali
hodnotitelům zpětnou vazbu ke kvalitě jejich práce;
vyvinout účelové nástroje IT pro spolehlivé řízení procesu hodnocení;
omezit, kolikrát může být návrh opakovaně předložen, čímž se uvolní zdroje, které
se v současné době využívají na opakované hodnocení téhož návrhu v několika po
sobě jdoucích uzávěrkách;
chce-li Komise podpořit účast více MSP s vynikajícími inovačními projekty, měla by
zveřejňovat míru úspěšnosti na projektový návrh.
Termín pro splnění doporučení: 2021
Účinnost podpory ve fázích nástroje pro MSP
Fáze 1 představuje účinnou podporu díky rychlému výběrovému řízení,
označení „EU“ pro příjemce a přístupu ke službám napomáhajícím zakládání a rozvoji
podniků. Kromě toho se díky fázi 1 zúčastnilo programu EU mnoho inovačních
společností. Příliš však zatěžuje Komisi při správě nástroje a podobné programy již
v některých zemích existují (viz body 81–89). Fáze 1 byla od září 2019 ukončena.
Fáze 2 nástroje pro MSP poskytuje příjemcům účinnou podporu a všechny
zainteresované subjekty ji v její současné podobě vysoce oceňují. Nabízí značku „EU“,
která společnosti a projekty zviditelňuje, pomáhá jim získat další investice a umožňuje
přístup k síti koučování a služeb napomáhajících zakládání a rozvoji podniků v EU (viz
body 91–94).
Doporučení 3 – Nahradit fázi 1 a dále rozvíjet podporu pro MSP
ve fázi 2
Komise by měla:
navrhnout členským státům, že bude řídit program podobný fázi 1;
poskytnout příjemcům tohoto programu přístup ke koučování a službám
napomáhajícím zakládání a rozvoji podniků a ke značce „EU“;
zachovat program podobný fázi 2 jako součást EIR v rámci programu Horizont
Evropa a rozvíjet výsledky z pilotního programu ERI.
Termín pro splnění doporučení: 2021
Koučování a služby napomáhající zakládání a rozvoji podniků mohou umocnit
účinky nástroje, jelikož však byly zahájeny pozdě, nejsou dostatečně viditelné a využila
je pouze malá část MSP. Zdroje na ně vyčleněné jsou omezené a služby by mohly být
více individualizovány a mohly by se lépe zaměřovat na poptávkovou stranu
(viz body 95–103).
Doporučení 4 – Posílit služby napomáhající zakládání a rozvoji
podniků
Komise by měla posílit služby napomáhající zakládání a rozvoji podniků tím, že na tuto
oblast vyčlení přiměřené zdroje, aby:
poskytovala služby napomáhající zakládání a rozvoji podniků více uzpůsobené
potřebám,
prostřednictvím cíleného komunikačního přístupu zvýšila informovanost příjemců,
lépe se zaměřila na poptávkovou stranu tím, že naváže kontakty s velkými
soukromými zákazníky, a prostřednictvím zadávání veřejných zakázek na
inovativní projekty.
Termín pro splnění doporučení: 2022
Získávání investic po financování z nástroje pro MSP
Většina příjemců podpory z nástroje pro MSP na svou inovační činnost stále
potřebuje další financování, aby mohla inovační projekty uvést na trh. Komise však
učinila málo proto, aby vytvořila vazby s finančními nástroji podporovanými EU, a
neprozkoumala možnosti spolupráce s národními podpůrnými institucemi. Příjemci
z velké části nemají o finančních nástrojích podporovaných EU povědomí a Komise má
o finančních potřebách příjemců jen malé znalosti. Skutečnost, že dostupné informace
o finančních nástrojích jsou zlomkovité a komunikace mezi soukromými a veřejnými
poskytovali kapitálu je omezená, potvrdila i zpráva EIB z roku 2018 o přístupu
k financování pro příjemce (viz body 107–116).
Doporučení 5 – Vytvořit vazby s finančními nástroji
Komise by měla:
pravidelně shromažďovat informace o objemu a druhu financování, které příjemci
podpory z programu pro MSP potřebují během realizace inovačního projektu;
zvýšit informovanost příjemců o existenci různých finančních nástrojů na úrovni
EU i na národní úrovni a poskytovat poradenství o těch z nich, které by nejlépe
odpovídaly jejich finančním potřebám;
určit a podporovat synergie s finančními nástroji podporovanými EU, a
podporovat tak příjemce podpory z programu pro MSP při získávání financování;
spolupracovat s členskými státy a národními podpůrnými institucemi s cílem
propagovat finanční nástroje podporované jednotlivými zeměmi, které by mohly
odpovídat finančním potřebám příjemců podpory z programu pro MSP.
Termín pro splnění doporučení: 2022
