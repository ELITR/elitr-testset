SHRNUTÍ
VI: Druhá odrážka – strategický rámec pro kulturní politiku v Evropské unii koncipovaný Evropskou
komisí (viz zejména „Nová evropská agenda pro kulturu“) se zaměřuje na přínos kultury pro evropské
společnosti, evropské hospodářství a mezinárodní vztahy Evropy. Nesoustředí se specificky na
propagaci kulturních památek.
Investice z EFRR do obnovy kulturních památek často umožňují, aby se tato místa následně zapojila
do kulturních iniciativ na úrovni EU.
Třetí odrážka: Hlavním cílem EFRR je přispět k utužení hospodářské, sociální a územní soudržnosti.
Podle bodu 11 preambule nařízení o EFRR je odůvodnění způsobilosti k financování z EFRR pro
financování kultury a podpory kulturního dědictví závislé na jejich začlenění do územní strategie pro
konkrétní oblast nebo na tom, do jaké míry přispívají k růstu podporujícímu zaměstnanost.
Kultura je navíc oblastí primární pravomoci členského státu, v níž musí Evropská komise dodržovat
zásadu subsidiarity.
VII: Operační programy a projekty EFRR mají převážně hospodářské a sociální cíle v souladu s
právním základem pro politiku soudržnosti stanoveným ve Smlouvě.
