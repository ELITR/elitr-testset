Nařízení o EFRR vyžaduje, aby se provádělo vnitrostátní nebo regionální
posuzování rizik pro zvládání katastrof s ohledem na přizpůsobení se změně klimatu.
Splnění tohoto požadavku je podmínkou účinného a účelného využívání prostředků
z EFRR v rámci tematického cíle podpory přizpůsobení se změně klimatu, předcházení
rizikům a jejich řízení. Rámec EFRR od členských států nevyžaduje, aby v rámci
celostátního nebo regionálního posuzování rizik braly v úvahu památky kulturního
dědictví, které jsou ve větší míře vystaveny rizikům přírodních nebezpečí. V nedávné
studii Komise v tomto ohledu uvedla, že ochrana kulturního dědictví před přírodními
i člověkem způsobenými katastrofami stále naráží na problém, že pro účely řízení rizik
v krizových situacích není kulturní dědictví chápáno či prakticky uplatňováno jako
dostatečně významná priorita 47. Komise se spolu s členskými státy touto otázkou
zabývala v prosinci 2019 a vypracovala pokyny k podávání zpráv o zvládání rizik
katastrof, které obsahují reference na památky kulturního dědictví a v nichž vybízí
členské státy k tomu, aby hlásily, mapovaly a sdělovaly potenciální dopad rizika
katastrofy na kulturní dědictví 48. Zatím není ještě jasné, jakým způsobem členské státy
tyto pokyny uplatní.
