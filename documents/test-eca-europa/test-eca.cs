s nimi stanoveny a využívány vhodné komunikační kanály.
Komise a EASME s omezeným rozpočtem prováděly komunikační činnosti a
uspořádaly různé akce, jichž se účastnili možní příjemci nástroje pro MSP; šlo hlavně
