POZNÁMKY
Strategický rámec pro opatření EU odráží Smlouvy a podpůrnou úlohu EU ve vztahu ke kultuře.
Stávající zpráva navíc analyzuje pouze finanční prostředky, které jsou k dispozici pro projekty v
oblasti kulturní infrastruktury (tj. ESI fondy), přičemž předpoklad, který se zde uvádí, se týká
financování od EU celkově.
V případě investic z EFRR je cílem sociálně-ekonomický rozvoj. Každá investice s kulturním
rozměrem musí přispět k dosažení tohoto cíle. Rámec pro podporu z EFRR je stanoven v příloze I
nařízení o společných ustanoveních.
Komise rozlišuje mezi rámcem EU pro investice do kulturních památek a rámcem EU pro opatření
EU v oblasti kultury.
Existuje pouze jeden strategický rámec EU pro kulturu, a tím je Nová evropská agenda pro
kulturu.
Jediným fondem EU specificky určeným pro kulturu je program Kreativní Evropa, který
zohledňuje stávající agendu EU pro kulturu.
Jiné fondy EU mají jiné cíle a závisí na jiných politických strategiích.
Žádná ze stávajících „etiket“ EU pro kulturu nebyla koncipována s perspektivou následné
mobilizace financování EU. Kulturní iniciativy Komise a financování z EFRR mají odlišné cíle,
nejsou vzájemně podmíněny.
Označení „Evropské dědictví“ a další iniciativy Komise, kupříkladu „Evropské hlavní město kultury“,
jsou často udělovány městům či památkám, které dříve využívaly investice z EFRR. Tato podpora z
EFRR jim umožnila následně získat „etiketu“. Dobrým příkladem je Vratislav, která se stala díky
investicím z EFRR v období 2007–13 evropským hlavním městem kultury pro rok 2016.
Vymezení kritérií pro výběr projektů i samotný výběr spadají do pravomoci členských států.
Monitorovací výbor prověří a schválí metodiku a kritéria pro výběr operací (čl. 110 odst. 2 nařízení o
společných ustanoveních).
Jednotlivé fondy EU jsou koncipovány tak, aby se vzájemně doplňovaly. Pokud jde o ESI fondy v
rámci sdíleného řízení, musely členské státy v dohodách o partnerství stanovit: „opatření v souladu s
institucionálním rámcem členských států, která zajišťují koordinaci mezi ESI fondy a dalšími unijními
a vnitrostátními nástroji financování a s EIB“ (čl. 15 odst. 1 písm. b) bod i) nařízení o společných
ustanoveních).
Požadavek zavést opatření, která zajistí koordinaci mezi ESI fondy a dalšími unijními a
vnitrostátními nástroji financování a s EIB, neznamená, že by měly být stanoveny detailní koordinační
mechanismy pro každý konkrétní druh investic financovaných z ESI fondů. To se týká zejména
investic, jako je podpora kulturních památek, jejichž velikost je ve většině programů EFRR omezena.
Viz odpověď Komise k bodu 23.
Nařízení o společných ustanoveních vyžaduje, aby členské státy oznamovaly Komisi systematicky
finanční údaje pro EFRR (šablony tabulek pro pravidelné podávání zpráv jsou stanoveny v
prováděcím nařízení Komise).
Na kulturní statistiky se nevztahuje žádná právní úprava EU. Většina statistik EU o kultuře je
odvozena z různých průzkumů a sběrů údajů, které jsou předmětem právní úpravy (povinné),
např. šetření pracovních sil v EU, strukturální podniková statistika, národní účty. Kulturní položky
však nelze ve výsledcích těchto průzkumů vždy zvlášť rozlišit (tj. údaje o kultuře nejsou v údajích
předávaných Eurostatu dostupné).
Komise připomíná, že záměrem EFRR není poskytnout žádný takový rámec pro kulturní památky.
Komise zdůrazňuje, že vymezení rámců pro investice do kulturních památek není v pravomoci EU,
nýbrž členských států.
Komise konstatuje, že omezení na kulturní infrastrukturu v malém měřítku nebylo součástí návrhu
Komise na stávající nařízení o EFRR (COM(2011) 614 final), ale bylo zavedeno spolunormotvůrci
během jednání s Evropským parlamentem a Radou.
Komise musí stanovit prioritu investic do kultury ve vztahu k jiným odvětvím, jako je doprava
nebo životní prostředí, a zajistit, aby existovaly podmínky pro maximalizaci dopadů těchto investic,
což může vést k určitému snížení.
Komise zdůrazňuje, že může pouze požadovat, aby členské státy koncipovaly své dohody o
partnerství a operační programy v souladu s nařízeními schválenými spolunormotvůrci. Nařízení o
společných ustanoveních vyžaduje, aby členské státy využívaly ESI fondy s cílem účinně přispívat ke
strategii Unie pro inteligentní a udržitelný růst podporující začlenění. Nevyžaduje sladění s některou
kulturní agendou.
Komise se domnívá, že účinnost investic z EFRR lze posuzovat pouze na základě cílů EFRR:
posílit hospodářskou, sociální a územní soudržnost.
Zaměření projektů financovaných z EFRR závisí na tematickém cíli, v jehož rámci jsou
financovány. Tematické cíle 1 až 3 se skutečně zaměřují na růst a konkurenceschopnost. Kulturní
projekty v rámci tematického cíle 6 byly ale zaměřeny na zachování a ochranu životního prostředí a
na podporu účinného využívání zdrojů. Nebo pokud se jedná o investici v rámci tematického cíle 9
(např. pro přístup ke kulturním službám), zaměřuje se na podporu sociálního začleňování, boj proti
chudobě a diskriminaci.
Společná odpověď k bodům 63 a 64
Komise zdůrazňuje, že i když některých ukazatelů výsledků nebylo v době, kdy byly podkladové
projekty ukončeny, dosaženo, mohlo jich být dosaženo v době, kdy byly příslušné programy
uzavřeny, neboť realizace výsledků nějakou dobu trvá.
Komise konstatuje, že existují regulatorní požadavky na stanovení ukazatelů výsledků u prioritních
os, ale nikoli na úrovni projektů.
Komise zdůrazňuje, že programové období 2014–2020 zavedlo striktnější požadavky na
vykazování údajů o výkonnosti. V systémech, které se používají ke shromažďování, ověřování a
vykazování ukazatelů, se provádějí audity na vnitrostátní úrovni a na úrovni EU. Nespolehlivost údajů
o výkonnosti je považována za slabinu v systému řízení a kontroly a může vést k pozastavení plateb.
Komise se domnívá, že i když stále existuje prostor pro zlepšení, jedná se o významné kroky a silnou
pobídku k tomu, aby programové orgány zvýšily spolehlivost.
Komise konstatuje, že v některých případech byla nezbytná částečná obnova v tom smyslu, že
absence zásahu by mohla vést k uzavření dané památky, a tudíž zvýšení počtu návštěvníků není vždy
cílem samo o sobě.
Společný ukazatel EFRR „zvýšení očekávaného počtu návštěv podporovaných památek, které jsou
součástí kulturního nebo přírodního dědictví, a atrakcí“, vyjadřuje předem odhadovaný nárůst počtu
návštěv dané památky v roce, který následuje po dokončení projektu. Řídicí orgány stanoví metodiku
pro odhad předpokládaného počtu, který může být založen na analýze poptávky. V rámci tohoto
společného ukazatele by řídicí orgány neměly vykazovat skutečný počet návštěvníků.
V rámci sdíleného řízení Komise sleduje souhrnné výstupy a výsledky na úrovni programů a
priorit. Pro stanovení ukazatelů výsledků na úrovni projektu neexistuje regulatorní požadavek. Dobře
koncipovaná intervenční logika zajistí, aby výstupy a výsledky na úrovni projektu přispívaly k
dosažení očekávaných výsledků operačních programů, které jsou rovněž ovlivňovány vnějšími
faktory. Komise se tudíž domnívá, že posouzení přínosu projektů k cílům programu, vyjádřené jejich
ukazateli výsledků, nelze měřit výkonností jednotlivých projektů, ale vyžaduje posouzení dopadů.
Komise sdílí toto znepokojení a připomíná, že příjemce má zákonnou povinnost vrátit příspěvek
EU v případě podstatné změny operace do pěti let od závěrečné platby příjemci (článek 71 nařízení o
společných ustanoveních). V tomto smyslu by absence údržby u obnovené kulturní památky mohla
představovat podstatnou změnu podmínek provádění operace, která narušuje původní cíle investice z
EFRR.
Přidělování EFRR projektům je založeno na cílech politiky soudržnosti a na specifických cílech
stanovených v operačních programech, které se na kulturu převážně nezaměřují.
Komise zdůrazňuje, že nemá pravomoc překročit požadavky na podávání zpráv stanovené v
článku 6 rozhodnutí 1313/2013/EU o mechanismu civilní ochrany Unie. To vyžaduje, aby členské
státy daly Komisi k dispozici souhrny svých posouzení rizik a posouzení schopností v oblasti řízení
rizik se zaměřením na hlavní rizika zjištěná na celostátní (nebo regionální) úrovni.
Společný ukazatel „zvýšení počtu návštěvníků památek“ je operačními programy používán, pouze
pokud je relevantní pro podporované operace. Podpora z EFRR vnitrostátním orgánům nebrání v
uplatňování opatření na ochranu památek kulturního dědictví.
Viz odpověď Komise k bodu 17.
Tato otázka je rovněž zmíněna v pravidlech pro udělování výjimek pro státní podporu, která musí
být při přiznání podpory z EFRR dodržena od roku 2014. Tato pravidla zejména stanoví, že pokud jde
o investice do kultury a zachování kulturního dědictví, výše podpory nepřesáhne rozdíl mezi
způsobilými náklady a provozním ziskem z investice.
Nedosažení ukazatelů výsledků u projektů v rámci programů EFRR na období 2007–2013 nemělo
žádné finanční důsledky, neboť takové ustanovení nebylo zahrnuto do nařízení z let 2007–2013. V
rámci sdíleného řízení mohou být pobídkové mechanismy, na něž odkazuje Evropský účetní dvůr,
používány pouze ve smluvních ujednáních mezi oprávněnou osobou projektu a řídicím orgánem.
Nařízení o společných ustanoveních vyžaduje, aby si operace spolufinancované z EFRR
zachovávaly svou povahu, cíle a prováděcí podmínky po dobu nejméně 5 let od závěrečné platby
příjemci. Dodržování tohoto regulatorního ustanovení sledují členské státy.
Stávající požadavek na posouzení dopadů všech intervencí EFRR pobízí členské státy k tomu, aby při
plánování a programování intervencí přijaly střednědobý až dlouhodobý výhled, a tudíž nepřímo
rovněž přispěly k udržitelnosti investičních výsledků. Přímé sledování udržitelnosti výsledků projektů
však nadále spadá do odpovědnosti členských států.
