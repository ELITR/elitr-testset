Glosář
Dohoda o partnerství (DoP): Dohoda mezi Komisí a členským státem nebo třetí zemí či
třetími zeměmi uzavřená v rámci výdajového programu EU, která stanoví například
strategické plány, investiční priority nebo obchodní podmínky či poskytování rozvojové
pomoci.
Evropské strukturální a investiční fondy (ESI fondy): Pět hlavních fondů EU, které
společně podporují hospodářský rozvoj v EU: Evropský fond pro regionální rozvoj,
Evropský sociální fond, Fond soudržnosti, Evropský zemědělský fond pro rozvoj
venkova a Evropský námořní a rybářský fond. Vztahuje se na ně společný soubor
předpisů.
Hospodářský cíl: Cíl obecně související s produktivitou a/nebo zaměstnaností.
Kulturní cíl: Cíl zabezpečit a posílit hmotnou a nehmotnou kulturní rozmanitost
(kulturní lokality, hudební představení, umělecké výstavy atd.).
Operační program (OP): Základní rámec provádění projektů soudržnosti financovaných
ve stanoveném období z prostředků EU, který odráží priority a cíle stanovené
v dohodách o partnerství mezi Komisí a jednotlivými členskými státy.
Sociální cíl: Cílem je poskytnout přístup do kulturních lokalit všem sociálním skupinám
(včetně znevýhodněných osob a osob se zdravotním postižením), demokratizovat
znalosti, podporovat vzdělávání a zaměstnanost.
