monitorovací inspekce provedené příjemcem došli odborníci k závěru, že stavba
byla narušena v důsledku velmi vysokého počtu návštěvníků, kteří mohli do
triclinia vstupovat, a v důsledku chybějící ochrany na všech stranách stavby.
V rámci projektu EFRR, který byl předmětem auditu, byla financována výroba
polykarbonátového ochranného zařízení, které však bylo uloženo ve skladu
a nebylo nainstalováno. K jeho instalaci došlo až na začátku roku 2019 během
údržby objektu, tedy tři roky po dokončení projektu. V důsledku tohoto pochybení
byla poškozena fyzická infrastruktura letního triclinia.
