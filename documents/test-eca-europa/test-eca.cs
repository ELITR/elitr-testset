investice k dispozici (EFRR), přičemž jsme posuzovali následující aspekty:
Část 1: Vhodnost kulturního rámce EU a koordinace s možnostmi financování investic
do kulturních lokalit
—
Vhodnost strategického a legislativního rámce zavedeného na úrovni EU pro
investice do kulturních lokalit financované z prostředků EU.
