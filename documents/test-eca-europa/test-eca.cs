na ochranu evropského kulturního dědictví
S ohledem na potřebu lépe zajistit plnění cíle Smlouvy o EU, jímž je ochrana
evropského kulturního dědictví, by Komise měla:
shromažďovat osvědčené postupy týkající se alternativních zdrojů financování od
členských států.
spolu s členskými státy prozkoumat možnost pracovat na rozvoji programu
financování založeného na soukromých zdrojích financování lokalit kulturního
dědictví.
koordinovat takové potenciální iniciativy s ostatními kulturními iniciativami EU,
(např. označení Evropské dědictví, Evropská hlavní města kultury).
Časový rámec: do prosince 2022.
Smíšená účinnost a udržitelnost kontrolovaných projektů EFRR
Navzdory snahám EU o zvýšení sociálních dopadů zásahů v kulturní oblasti jsou
cíle operačních programů (OP) a projektů EFRR většinou ekonomické. Investice do
kulturních lokalit jsou považovány za zdroj umožňující zvyšování konkurenceschopnosti
nebo místní územní rozvoj. Kulturním aspektům je v operačních programech, které
byly předmětem našeho auditu, věnována nejmenší pozornost, a to dokonce i tehdy,
když jsou jasně označeny jako cíle. Řídicí orgány EFRR často nepovažují kulturní aspekty
při vybírání projektů za relevantní hledisko (body 52–61).
Výkonnost projektu nebylo možné posoudit u všech dokončených
kontrolovaných projektů. Projekty byly v době auditu funkční, ale zjistili jsme několik
slabých stránek ve výběru a vykazování ukazatelů, což omezovalo použitelnost
vykazovaných údajů k vyvození závěru o výkonnosti projektu. Navíc vzhledem k povaze
ukazatelů výsledků EFRR nemusí dosažení cílů OP vždy záviset přímo na výkonnosti
projektu (body 62–67).
Požadavky EFRR se nezabývají fyzickou ochranou financovaných kulturních
lokalit. Přestože památky kulturního dědictví potřebují neustálou údržbu a potýkají se
s různými riziky, pokud jde o jejich zachování, u žádného z projektů EFRR, které byly
předmětem našeho auditu, nebylo nutné prokazovat, jak bude zajištěna ochrana dané
kulturní lokality po dokončení projektu, a při žádosti o finanční prostředky EU nebylo
nutné předkládat plán údržby. EFRR navíc nemůže financovat ochranu ohrožených
lokalit, nemá-li taková činnost bezprostřední hospodářský a sociální efekt, a stejně tak
ani program Kreativní Evropa. V minulosti nabízel takovou možnost dřívější evropský
fond Raphael. Zajišťování ekonomických přínosů, jemuž často slouží strategie podpory
cestovního ruchu, může být z hlediska ochrany některých lokalit, které se již nyní
potýkají s problémem masové turistiky, kontraproduktivní (body 68–75).
EU nedávno zvýšila své úsilí, které věnuje zajišťování zachování kulturního
dědictví. Na úrovni členských států nicméně rámec EFRR od členských států
nevyžaduje, aby v rámci vnitrostátního nebo regionálního posuzování rizik braly
v úvahu památky kulturního dědictví, které jsou ve větší míře vystaveny přírodním
nebezpečím (body 76–78).
Provozování a financování investičních nákladů kulturních lokalit, které byly
předmětem našeho auditu, jsou obecně závislé na veřejných dotacích. Rámec EFRR
neposkytuje příjemcům podporu na zvýšení jejich příjmů. Požadavky EFRR na projekty
vytvářející příjmy jsou stanoveny tak, že čím vyšší jsou čisté příjmy z projektu, tím
menší je podpora, kterou poskytuje EU. Ve výběrových řízeních, která byla předmětem
našeho auditu, byly také činnosti vytvářející příjmy podpořeny jen zřídkakdy (body 79–
88).
Sledování projektů po jejich dokončení probíhá jen v omezené míře.
Monitorovací systémy navštívených členských států umožňují prověřovat výkonnost
projektů ad hoc během pětileté doby životnosti, kterou ukládají právní předpisy EU,
avšak po uplynutí této lhůty se výkonnost projektu již nesleduje. V žádném
z kontrolovaných projektů navíc výkonnost projektu neovlivnila výši prostředků, které
příjemci obdrželi z EFRR (body 89–93).
Doporučení 3 – Posílit finanční udržitelnost kulturních lokalit
financovaných z EFRR
S ohledem na to, aby příjemci nebyli odrazováni od zvyšování vlastních příjmů, by
Komise měla prozkoumat a navrhnout zjednodušené formy podpory z EFRR pro
investice do kulturních lokalit.
V zájmu snížení závislosti na veřejných dotacích by financování z EFRR mělo ve fázi
výběru projektů upřednostňovat projekty, které obsahují plány na zlepšení finanční
soběstačnosti kulturních lokalit (např. diverzifikace zdrojů příjmů a větší spoléhání na
vlastní zdroje příjmů).
Časový rámec: v dostatečném předstihu s ohledem na sjednávání operačních
programů na období 2021–2027.
Doporučení 4 – Provést cílenější kroky na podporu ochrany
památek kulturního dědictví
Při sjednávání operačních programů by Komise měla členským státům doporučit, aby
zahrnuly památky kulturního dědictví do celostátního nebo regionálního plánu pro
zvládání katastrof, které vyžaduje navrhované nařízení o společných ustanoveních. To
by členské státy motivovalo ke zjišťování rizik pro zachování památek kulturního
dědictví a sestavení plánu vhodných opatření ke zmírnění těchto rizik.
Časový rámec: v dostatečném předstihu s ohledem na sjednávání operačních
programů na období 2021–2027.
