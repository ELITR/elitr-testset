Větší zaměření na MSP a inovace v programu Horizont 2020
Program Horizont 2020 je osmým rámcovým programem EU pro výzkum.
S rozpočtem ve výši 76,4 miliardy EUR na období 2014–2020 je největším veřejným
programem v oblasti výzkumu a inovací na světě 6.
V programu Horizont 2020 se na inovace klade větší důraz než v předchozích
programech, což se projevuje tím, že program poskytuje více prostředků na testování,
tvorbu prototypů, výzkum a vývoj orientovaný na podnikání a podporu inovačního
podnikání. V programu Horizont 2020 jsou také stanoveny ambicióznější cíle, pokud
jde o prostředky přidělené na MSP, než v kterémkoli z předchozích rámcových
programů: MSP by měly získat nejméně 20 % celkového úhrnného rozpočtu ve výši
miliard EUR na pilíře „vedoucí postavení v základních a průmyslových technologiích“
a „společenské výzvy“.
Na nástroj pro MSP byl vyčleněn rozpočet ve výši 3 miliardy EUR, což se rovná
% cíle pro MSP v rámci celého programu Horizont 2020 7.
