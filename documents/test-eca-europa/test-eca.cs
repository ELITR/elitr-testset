Krátká historie nástroje pro MSP
Nástroj pro MSP zahájil svou činnosti v roce 2014 a jeho koncepce vycházela
z programu SBIR v USA 9. Jeho „trychtýřový model“ spočívá v tom, že se provede velký
počet malých investic do studií proveditelnosti slibných inovačních projektů (fáze 1) a
nejlepší z nich postoupí do fáze 2 s vyšším objemem financování. Ve fázi koncepce
nástroje však byla povinnost projít před fází 2 fází 1 odstraněna, takže žadatelé mohou
přímo žádat ve kterékoli z fází.
Pokud jde o další části programu Horizont 2020, nástroj pro MSP se provádí na
základě pracovních programů, které se vztahují na období dvou či tří let a s každým
pracovním programem mohou být charakteristiky nástroje upraveny podle záměrů
příslušné politiky.
V pracovních programech na období 2014–2015 a 2016–2017 byl rozpočet
nástroje pro MSP rozdělen do tematických oblastí jako například biotechnologie, zdraví
či bezpečnost. MSP podávaly žádosti v oblastech, které nejlépe odpovídaly jejich
projektu.
