Smíšená účinnost a udržitelnost kontrolovaných projektů EFRR
Účinnost projektů EFRR, které se týkaly kulturních lokalit, jsme posuzovali podle
plnění cílů a zamýšlených výsledků těchto projektů. Posuzovali jsme zejména plnění
hospodářských, sociálních a kulturních cílů. Provádění těchto cílů má vnější dopady na
společnost.
Udržitelnost jsme posuzovali jako přetrvávající provozuschopnost dané lokality,
která vyžaduje pravidelnou údržbu, a v případě potřeby také obnovu fyzické
infrastruktury. Nezbytná je rovněž dostupnost finančních a lidských zdrojů. Tyto
činnosti tvoří vnitřní podmínky kulturní lokality.
Popsané cíle a činitele udržitelnosti spolu vzájemně souvisejí (viz grafické
znázornění 2). Investiční projekt v oblasti kultury může mít v zásadě účinky na dosažení
všech tří cílů i na udržitelnost kulturní lokality. Tyto účinky však mohou jít
protichůdným směrem. Hospodářské cíle mohou zvýšit finanční udržitelnost lokality,
ale mohou ohrožovat její zachování (např. z důvodu nadměrného cestovního ruchu,
snížení nákladů na údržbu). Některé sociální cíle mohou vést ke snížení finanční
udržitelnosti (např. snížením vstupného) nebo k omezení ochrany lokality (např.
v důsledku příliš vysoké návštěvnosti). Kulturní cíle mohou posílit sociální začleňování
a zvýšit hospodářský přínos, ale také mohou mít negativní dopad na finanční
udržitelnost lokality (např. kvůli zvýšení nákladů spojenému s rozšířením nabídky
kulturních aktivit).
