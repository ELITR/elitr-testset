Vyhovující rámec pro investice EU do kulturních památek má zásadní význam pro
to, aby umožňoval maximalizovat dopad investic na dosažení cílů, které rámec stanovil.
To vyžaduje jasný strategický rámec pro činnost EU v oblasti kultury s dobře fungující
koordinací regulačních rámců (tj. právní úpravou fondů EU) a neregulačních rámců
