Kultura a kulturní lokality
Většina Evropanů se domnívá, že kulturní dědictví je důležité nejen pro ně
samotné, ale také pro jejich společenství, region, zemi a EU jako celek 1. Kultura je
rovněž určitým zdrojem. Podle Komise zvyšuje růst a zaměstnanost, umožňuje sociální
začlenění a je hodnotou významnou z hlediska posilování mezinárodních vztahů EU 2.
Podle statistik Eurostatu bylo v EU v roce 2018 zaměstnáno v odvětví kultury 8,7
milionu lidí, což je 3,8 % celkové zaměstnanosti 3.
Kultura je široký pojem, který zahrnuje různé aktivity (např. řemesla, umění
a audiovizuální tvorbu) spadající do různých hospodářských odvětví (např. výroba,
služby a komunikace). V této zvláštní zprávě je výrazem „kulturní lokality“ míněna
hmotná infrastruktura, v níž se mohou Evropané setkávat s kulturou. Rozlišujeme mezi
památkami kulturního dědictví (staré historické lokality) a novou kulturní
infrastrukturou (nové budovy využívané pro výstavy umění, hudbu či divadlo atd.).
Pro politiku EU v oblasti kultury byl důležitým momentem rok 2017, kdy byli
představitelé EU vyzváni k tomu, aby oblasti vzdělávání a kultury věnovali více
pozornosti 4. Nedávno se Evropská rada ve své Nové strategické agendě na období
2019–2024 zavázala „investovat do kultury a našeho kulturního dědictví, které jsou
základem naší evropské identity“ 5.
