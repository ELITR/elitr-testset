Rámec politiky
Smlouvy stanoví, že EU „respektuje svou bohatou kulturní (…) rozmanitost a dbá
na zachování a rozvoj evropského kulturního dědictví” 6 a že EU musí podporovat
kulturu v členských státech 7. Smlouvy rovněž vymezují činnosti EU v této oblasti (šíření
kultury, kulturní dědictví, nekomerční kulturní výměny, umělecká a literární tvorba,
vnější spolupráce). Obsahují také obecný požadavek, aby činnosti v jiných oblastech
politiky zohledňovaly kulturní aspekty, což je požadavek označovaný jako
„začleňování“ 8.
EU nemá v oblasti kultury žádnou legislativní pravomoc. Odpovědnost za tvorbu
politik mají v této oblasti členské státy. V roce 2017 vyčlenily členské státy 1 % svých
státních výdajů na kulturní služby; jinak řečeno, na podporu kulturních aktivit bylo
v přepočtu na jednoho občana EU vynaloženo 132 EUR. Z této částky bylo zhruba 15 %
vynaloženo na kulturní investice (tj. pořízení, výstavbu nebo obnovu kulturních statků,
včetně fyzické práce na kulturních lokalitách). Příloha I obsahuje podrobnější
informace o státních výdajích na kulturní služby a o kulturních investicích v celé EU.
