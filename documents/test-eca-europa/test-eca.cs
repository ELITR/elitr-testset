nemusí být součástí jeho strategie financování.
Zkoumali jsme, zda byl nástroj pro MSP propagován, prováděn a monitorován
tak, aby zajistil snadný přístup MSP k nástroji, jak to stanoví rozhodnutí Rady
743/2013. Zjišťovali jsme také, nakolik byly používány marketingové techniky, aby se
