V oblasti kultury je obecným cílem EU, který je stanoven ve
Smlouvě, respektovat bohatou kulturní rozmanitost a dbát na
zachování a rozvoj evropského kulturního dědictví. Kultura spadá
do kompetence členských států. Unie může pouze napomáhat
jejich vzájemné spolupráci a podporovat nebo doplňovat jejich
činnosti.
Posuzovali jsme hospodářské, sociální a kulturní dopady investic
EFRR na tyto kulturní lokality a jejich finanční a fyzickou
udržitelnost. Prověřili jsme práci Komise a posoudili 27 projektů
ze sedmi členských států.
Při auditu se dospělo k závěru, že stávající rámec není dostatečně
zacílený a že pro zajištění účinnosti a udržitelnosti investic EFRR
do kulturních lokalit je potřeba větší koordinace.
