Udržitelnosti kulturních lokalit není věnována dostatečná pozornost
Udržitelnost investic EU musí být zajištěna ve všech fázích projektu. Prvním
krokem je ověřit už ve fázi žádosti, že příjemci jsou schopni danou lokalitu provozovat
nebo v jejím provozování pokračovat. Z tohoto důvodu by kulturní lokality využívající
investice z EU měly usilovat o co největší finanční soběstačnost 43 (tj. o to, aby ze svých
provozních příjmů byly schopny pokrýt své provozní náklady). Zásadní význam pro
zachování kulturních památek má jejich udržování, které by proto mělo být řádně
naplánováno, aby se omezily náklady na budoucí práce a aby se stav lokality pokud
možno nezhoršoval. Aby bylo možné posuzovat, zda výsledky investic EU přetrvávají,
měla by být zavedena monitorovací opatření.
