I Většina Evropanů považuje kulturní dědictví za důležité nejen pro ně samotné, ale
také pro jejich společenství, region, zemi a EU jako celek. Kultura je široký pojem, který
zahrnuje různé činnosti. V této zvláštní zprávě je výrazem „kulturní lokality“ míněna
hmotná infrastruktura, v níž se mohou Evropané setkávat s kulturou.
II Kulturní rámec EU je primárně definován Smlouvami. Smlouvy stanoví pro EU
obecný cíl respektovat svou bohatou kulturní rozmanitost a dbát na zachování a rozvoj
evropského kulturního dědictví. Oblast kultury je hlavně v pravomoci členských států.
Unie může pouze napomáhat spolupráci mezi členskými státy a podporovat nebo
doplňovat jejich činnosti.
III Účinnost a udržitelnost investic EFRR do kulturních lokalit jsme posuzovali
hodnocením vhodnosti kulturního rámce EU, jeho koordinace s možnostmi financování
a provádění financování z prostředků EFRR.
IV Audit se zaměřil na hospodářské, sociální a kulturní dopady těchto investic a na
finanční a fyzickou udržitelnost příslušných lokalit. Prověřili jsme práci Komise
a posoudili 27 projektů ze sedmi členských států. Vedli jsme rovněž rozhovory
s odborníky v této oblasti.
V Při auditu se dospělo k závěru, že současný rámec postrádá zacílení a pro zajištění
účinnosti a udržitelnosti investic EFRR do kulturních lokalit je potřeba jej lépe
koordinovat. Zjistili jsme níže uvedené skutečnosti.
VI Pokud jde o rámec pro investice EU do kulturních lokalit:
o
Zastřešující strategie Komise EU 2020 se oblastí kultury nezabývá. Základní
strategický rámec EU pro kulturu je složitý a ve financování EU se odráží pouze
částečně. Přenesení jeho cílů do tvorby politik na úrovni členských států je podle
Komise i nadále výzvou.
o
Komise vyvíjí několik iniciativ, které mohou podpořit kulturní lokality, ale kulturní
iniciativy EU mají na dostupnost finančních prostředků EU pro příjemce omezený
dopad. Nařízení o EFRR neobsahuje ustanovení na podporu kulturních lokalit
zapojených do kulturních iniciativ EU. Omezená je také koordinace mezi
samotnými fondy EU pro investice do kulturních lokalit.
o
Investice do infrastruktury jsou na úrovni EU financovány hlavně prostřednictvím
EFRR, který je významným zdrojem financování veřejných investic do kulturních
lokalit přibližně pro jednu třetinu členských států. Investice do kulturních památek
však nejsou považovány za prioritu EFRR, protože EFRR podporuje jiný cíl
Smlouvy, a to posilování sociální a hospodářské soudržnosti. Na vnitrostátní
úrovni jsme objevili příklady iniciativ členských států, které byly zaměřeny na
financování kulturních lokalit s využitím soukromých prostředků.
VII Pokud jde o účinnost a udržitelnost kontrolovaných projektů EFRR:
o
Navzdory snahám EU o zvýšení sociálních dopadů zásahů v kulturní oblasti jsou
cíle operačních programů (OP) a projektů EFRR většinou ekonomické. Na kulturní
hledisko se kontrolované OP zaměřují nejméně a pro většinu řídicích orgánů není
při výběru projektů žádným kritériem.
o
Výkonnost projektu bylo možné posoudit pouze u některých dokončených
kontrolovaných projektů. Projekty byly funkční, ale zjistili jsme několik slabých
stránek ve výběru a vykazování ukazatelů, což omezovalo použitelnost
vykazovaných údajů k vyvození závěru o výkonnosti projektu.
o
EFRR nemůže financovat ochranu ohrožených lokalit, pokud by taková činnost
neměla bezprostřední hospodářský a sociální účinek; totéž platí o programu
Kreativní Evropa. Zajišťování hospodářských přínosů, k čemuž často slouží
strategie podpory cestovního ruchu, může být z hlediska ochrany památek
kulturního dědictví kontraproduktivní.
o
Provozování a financování investičních nákladů kulturních lokalit, které byly
předmětem našeho auditu, jsou obecně závislé na veřejných dotacích. Současný
rámec financování dostatečně nemotivuje k vytváření příjmů. Požadavky EFRR na
projekty vytvářející příjmy jsou stanoveny tak, že čím vyšší jsou čisté příjmy
z projektu, tím menší je podpora, kterou poskytuje EU. Ve výběrových řízeních,
která byla předmětem našeho auditu, byly také činnosti vytvářející příjmy
podpořeny jen zřídkakdy.
