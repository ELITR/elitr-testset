Prezentace před porotou značně zlepšuje výběrové řízení, přičemž lhůta
pro udělení grantu se dodržuje
Zavedení prezentace projektů před porotou v roce 2018 bylo pozitivní změnou,
neboť vyvažuje nedostatky vlastní vzdálenému hodnocení a umožňuje ověření
praktických aspektů projektu a schopností týmu realizovat jej. Některé prvky přesto
nejsou optimální.
Členové poroty posuzují 20 prezentací projektů během jednoho hodnoticího
týdne a předpokládá se, že předem přečtou dokumenty předložené žadateli. Členové
porot, s nimiž jsme vedli rozhovory, se však domnívali, že dokumenty neobdrželi
s dostatečným předstihem, aby mohli prostudovat možných 1 600 stran informací
v žádostech.
Navrhli taky, že by měli mít přístup k připomínkám ze zpráv vzdálených
hodnotitelů. Byly by tak zachovány užitečné poznatky a zvýšila by se efektivnost
vzhledem k tomu, že na přípravu a projednání prezentací projektů, mají poroty jen
omezenou dobu. Podobně hodnotitelé také soudili, že kvalita jejich hodnocení by se
zlepšila, pokud by měli k návrhům, které hodnotili, k dispozici připomínky členů poroty.
Žádosti, které se dostanou do fáze prezentace, nejsou podrobovány žádné
hloubkové kontrole. Členové porot uvedli, že by bylo potřeba zavést „lehkou“
hloubkovou kontrolu, aby se ověřila správnost informací, které žadatelé poskytují
o patentech, profilu společnosti a složení týmu.
Komise stanovila lhůtu pro udělení grantu (dobu mezi uzávěrkou a podpisem
grantové dohody) na maximálně tři měsíce pro fázi 1 a šest měsíců pro fázi 2. Skutečná
doba pro udělení grantu od zahájení programu navzdory zavedení prezentace před
porotou v roce 2018 poklesla; v tomto roce podepsala agentura EASME 90 %
grantových dohod ve stanovené lhůtě.
