I Nástroj pro MSP byl zřízen na základě rámcového programu pro výzkum Horizont
a slouží k podpoře inovací v malých a středních podnicích (MSP). Jeho cílem je
rozvinout a využívat potenciál MSP zaplněním mezery ve financování vysoce rizikových
projektů v raném stadiu a zvýšením obchodního využití výsledků výzkumu
v soukromém sektoru. Je zaměřen na inovační MSP v EU a 16 přidružených zemích,
které vykazují velké úsilí rozvíjet se, růst a působit na mezinárodní úrovni ve všech
možných oblastech inovací.
II Nástroj, jehož rozpočet na období 2014–2020 činí 3 miliardy EUR, poskytuje granty
společnostem s vysokým potenciálem, které slouží jako podpora při vypracování studie
proveditelnosti (až 50 000 EUR ve fázi 1) nebo při provádění výzkumu a vývoje a
testování trhu (až 2,5 milionu EUR ve fázi 2). Podporu lze rovněž poskytovat v podobě
koučování a mentorství či jiných služeb napomáhajících zakládání a rozvoji podniků
(fáze 3).
III Při tomto auditu jsme posuzovali, zda nástroj pro MSP podporuje inovace MSP.
Zkoumali jsme, zda se zaměřuje na správný typ MSP, zda dosáhl širokého zeměpisného
pokrytí, zda byly proces výběru a podpora Komise účinné a zda Komise nástroj vhodně
monitorovala a následně kontrolovala, aby jej mohla zlepšovat. Předpokládáme, že
připomínky plynoucí z našeho auditu a doporučení, která předkládáme, se stanou
součástí debaty o tom, jak koncipovat a řídit nástroj, který od roku 2020 nástroj pro
MSP nahradí.
IV Zjistili jsme, že nástroj pro MSP účinně podporuje tyto podniky při rozvoji jejich
inovačních projektů a že značka „EU“, kterou přináší poskytování podpory EU, pomáhá
společnostem přilákat další investice.
V Nicméně obecné cíle nástroje spolu se změnami provedenými během jeho
provádění vyvolaly u zainteresovaných subjektů určitou nejistotu. Identifikovali jsme
riziko, že z nástroje se budou financovat některé MSP, které mohly financování nalézt
na trhu.
VI Účast v nástroji se mezi zeměmi zřetelně liší. Částečně to způsobují vnější činitele,
ale také rozdílná úroveň podpory, kterou poskytují národní kontaktní místa a omezení
v marketingových a komunikačních činnostech Komise.
VII Zjistili jsme, že během fungování nástroje byly zlepšeny postupy pro hodnocení a
výběr. Obzvláště užitečnou novinkou je ústní prezentace návrhů před porotou, protože
pomáhá určit nejlepší návrhy. Chybí však obousměrná zpětná vazba mezi fázemi
hodnocení. Navíc některé nástroje IT nevyhovují svému účelu, což oslabuje proces,
který je již pod tlakem.
VIII Opětovné předkládání neúspěšných návrhů výrazně a rostoucí měrou zatěžuje
zdroje určené na řízení a hodnocení, aniž přináší přidanou hodnotu. Nejenže zvyšuje
správní náklady, ale snižuje také míru úspěšnosti, a odrazuje tak od účasti.
IX Fáze 1 nástroje představuje účinnou podporu díky jednoduchému a rychlému
výběrovému řízení, označení „EU“ a přístupu ke službám napomáhajícím zakládání a
rozvoji podniků. Vede však k neúměrně vysokým správním nákladům na administrativu
Komise a v zemích, kde podobné programy již existují, je její význam oslaben.
X Fáze 2 nástroje, která poskytuje vyšší míru podpory s cílem uvést inovace na trh,
přináší tytéž kladné výsledky jako fáze 1 a také MSP pomáhá získat další investice.
XI Nicméně jsme zjistili, že většina příjemců na svou inovační činnost stále potřebuje
další financování, aby své projekty mohla uvést na trh. Komise učinila málo proto, aby
vytvořila vazby mezi potřebami MSP v oblasti financování a finančními nástroji
podporovanými EU, a o finančních potřebách příjemců má jen malé znalosti.
XII Koučování a služby napomáhající zakládání a rozvoji podniků mohou umocnit
účinky nástroje, jelikož však byly zahájeny pozdě, využila je pouze malá část MSP.
Kromě toho nebyly dostatečně přizpůsobeny potřebám příjemců.
XIII Získané investice a vývoj společností se monitorují nákladově efektivním
způsobem, avšak neposuzuje se při tom skutečný dopad nástroje. I když se příjemcům
dařilo získávat kromě poskytnutých grantů i další investice, mezi účastnickými zeměmi
existují rozdíly, přičemž MSP v severní a západní Evropě získávají více soukromého
financování než MSP na jihu a východě Evropy.
XIV Ve zprávě doporučujeme, aby Komise:
—
zlepšila marketingovou a komunikační strategii nástroje,
—
zlepšila podporu, kterou poskytuje národním kontaktním místům a síti Enterprise
Europe Network (EEN), a vylepšila výběrové řízení nástroje, aby se lépe využívaly
zdroje a financovaly nejlepší návrhy,
—
omezila, kolikrát může být návrh opakovaně předložen, a zveřejňovala míru
úspěšnosti na projektový návrh,
—
navrhla členským státům, že bude řídit programy podobné fázi 1,
—
v příštím programovém období zachovala program podobný fázi 2 a rozvíjela
stávající výsledky,
—
posílila služby napomáhající zakládání a rozvoji podniků tím, že na tuto oblast
vyčlení přiměřené zdroje,
—
určí synergie mezi nástrojem pro MSP a finančními nástroji podporovanými EU a
bude je podporovat.
