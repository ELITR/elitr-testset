ODPOVĚDI KOMISE NA ZVLÁŠTNÍ ZPRÁVU EVROPSKÉHO ÚČETNÍHO DVORA
„NÁSTROJ PRO MALÉ A STŘEDNÍ PODNIKY V AKCI: ÚČINNÝ A INOVAČNÍ
PROGRAM ČELÍCÍ VÝZVÁM“
I. Nástroj pro malé a střední podniky (MSP) je první režim podpory v historii rámcového programu
pro výzkum individuálně zaměřený na inovativní MSP a prováděný prostřednictvím výkonné
agentury. MSP, jejichž podpora nebyla dostatečná v předchozích rámcových programech, se
hromadně přihlásily do tohoto nového režimu podpory, který byl v průběhu času doladěn tak, aby lépe
odpovídal jejich skutečným potřebám. Velký zájem k datu uzávěrky o první pilotní nástroj
Accelerator Evropské rady pro inovace (ERI) dokládá, že cesta, kterou se Komise vydala, odpovídá
očekáváním inovativních MSP.
ÚVOD
Komise provádí testování finanční podpory projektů prostřednictvím kapitálového financování od
října 2019.
PŘIPOMÍNKY
Členové iniciativy pro přístup k rizikovému financování a programového výboru pro MSP – kteří
jsou často také národními kontaktními místy – byli o změnách zaváděných v nástroji pro MSP
pravidelně informováni.
GŘ RTD a výkonná agentura pro malé a střední podniky (EASME) dále k těmto změnám provedly
komunikační kampaně, aby zvýšily povědomí zainteresovaných stran včetně národních kontaktních
míst a Enterprise Europe Network (EEN).
Nástroj pro MSP cílí na všechny typy inovativních MSP, které vykazují velkou snahu růst,
rozvíjet se a působit na mezinárodní úrovni.
V souladu s politickými prioritami nově zvolené předsedkyně Komise se očekává provedení zvláštní
výzvy pro ERI Accelerator pro rok 2020 podmíněné schválením výboru pro strategický program.
Komise poukazuje na to, že toto riziko je řešeno v pilotním programu ERI od roku 2019.
Také zdůrazňuje, že je obrovský rozdíl mezi vnímáním příjemců podpory pro MSP a skutečností,
pokud jde o získávání finančních prostředků z rizikového kapitálu nebo od neformálních investorů
(tzv. andělských investorů), kde je míra úspěšnosti poměrně nízká.
Komise zdůrazňuje, že vynaložila velké úsilí na propagaci posíleného pilotního programu ERI
organizováním pracovních seminářů pro MSP v rámci různých technologických konferencí po celé
Evropě (včetně konferencí Slush, Wolves Summit, Smart City). Změny zaváděné v posíleném
pilotním projektu ERI byly oznamovány během putovních prezentací a specializovaných akcí
pořádaných ve všech členských státech.
Od zahájení programu Horizont 2020 bylo v členských státech a přidružených zemích uspořádáno
více než 65 informačních akcí. Informační kampaň Komise v členských státech, organizovaná
společně s národními kontaktními místy, byla v roce 2019 posílena účastí MSP. Komise zahájila řadu
jednorázových konzultací se zainteresovanými stranami na vnitrostátní a regionální úrovni, které se v
členských státech konají od června 2019. Cílem je vytvořit společnou koncepci pro způsoby
provádění programu Horizont Evropa, přičemž v jejím rámci bude určen další vývoj nástroje pro
MSP.
Komise se domnívá, že povědomí národních kontaktních míst a EEN o změnách zaváděných v
koncepci nástroje pro MSP pomohla zvýšit koordinační a podpůrná iniciativa Access4SME.
Agentura EASME skutečně musela každý rok vyměnit 25 % hodnotitelů, aby dosáhla souladu s
pravidly programu Horizont 2020.
Na potřebu školení pro nové hodnotitele reagovala agentura EASME tak, že od roku 2018 v Bruselu
pořádá expertní setkání, kam zve zkušené i nové odborníky, aby se školili a aby byla zajištěna
synergie.
Na základě zpětné vazby od členů poroty zavedla Komise v říjnu 2019 praxi, kdy členové poroty
mají dva celé týdny na důkladné posouzení návrhů.
Pro řešení problému, na který EÚD poukazuje v tomto odstavci, zavedla Komise do posíleného
pilotního programu ERI proces hloubkové kontroly. Od října 2019 jsou v rámci posíleného pilotního
projektu ERI, který nabízí kombinované financování, od žadatelů požadovány podrobnější informace
o vlastnictví, právech duševního vlastnictví a složení týmu. Tyto aspekty nyní hodnotitelé posuzují
během vzdáleného hodnocení a pohovorů.
Ačkoli to není stanoveno v právním základu nástroje pro MSP, Komise může se schválením
příslušného programového výboru navrhnout postup, který omezí počet opakovaně předkládaných
návrhů.
Komise zdůrazňuje, že zahájení poskytování služeb napomáhajících zakládání a rozvoji podniků
bylo důkladně připravováno a předcházelo mu otevřené zadávací řízení.
Agentura EASME shromažďuje informace o dalších finančních potřebách některých příjemců v
období realizace grantu i po něm, a to prostřednictvím několika kanálů (přihlášky na investorské akce
služeb napomáhajících zakládání a rozvoji podniků a profily společností v rámci EU nástroje pro
párování investorů s rozvíjejícími se společnostmi). Kromě toho musí společnosti od uzávěrky z října
poskytovat informace o tom, jak hodlají financovat veškerý potřebný rozvoj do doby jejich
vstupu na trh, a to nad rámec požadovaného grantu a/nebo vlastního kapitálu. Tyto informace zahrnují
jak údaje o potřebných částkách, tak o zdrojích potenciálního financování.
Komise začala řešit problém, který EÚD uvádí v tomto odstavci, prováděním posíleného
pilotního programu ERI v roce 2019.
