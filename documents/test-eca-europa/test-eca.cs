a navržena odpovídající opatření. Cíle by měly být realistické a k jejich uskutečňování
by měly být zvoleny vhodné politické nástroje a způsoby financování. Požadavky na
zúčastněné strany by měly být stanoveny v souladu s jejich pravomocemi. Sledování
tohoto rámce je pro hodnocení pokroku při plnění cílů zásadní.
