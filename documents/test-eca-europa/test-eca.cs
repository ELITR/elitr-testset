Monitorovací systémy navštívených členských států nezajišťují po dokončení
projektu systematickou kontrolu jeho výkonnosti. Během pětileté doby životnosti
projektů lze jejich výkonnost kontrolovat ad hoc podle uvážení jednotlivých států a po
uplynutí této lhůty se užitkovost projektu již nesleduje.
Na projektové úrovni byla pouze jedna z navštívených kulturních lokalit
předmětem následného posouzení, které analyzovalo hospodářské, sociální nebo
kulturní dopady projektu EFRR.
Pokud jde o památky kulturního dědictví, monitorovací opatření EFRR se výrazně
liší od opatření platných pro památky UNESCO, Kulturní stezky Rady Evropy i od
pravidel samotné Komise, která se vztahují na lokality s označením „Evropské
dědictví“. Přestože EFRR poskytuje finanční prostředky, nevyžaduje po dokončení
projektu žádné časté monitorování. Postupy členských států v této oblasti se
v pětiletém období udržitelnosti výrazně liší. V programovém období 2014–2020 dva
navštívené členské státy monitorují některé projekty na místě, zatímco jeden stát
provádí pouze správní kontroly na základě zpráv předložených příjemci. Výše uvedené
mezinárodní značky naopak vyžadují, aby bylo monitorování prováděno systematicky,
a to i přesto, že nejsou spojeny s poskytováním finančních prostředků (viz příloha VII).
Závěry a doporučení
Celkově dospěl audit k závěru, že neexistuje žádný vhodný rámec pro zajištění
účinnosti a udržitelnosti investic EFRR do kulturních lokalit.
Chybí zacílení a dostatečná koordinace investic EU do kulturních lokalit
Smlouvy stanoví obecný cíl, že EU respektuje svou bohatou kulturní rozmanitost
a dbá na zachování a rozvoj evropského kulturního dědictví. Oblast kultury je hlavně
v pravomoci členských států. Unie může pouze napomáhat spolupráci mezi členskými
státy a podporovat nebo doplňovat jejich činnosti (body 05–16).
Zastřešující strategie Komise EU 2020 se oblastí kultury nezabývá. Základní
strategický rámec EU pro kulturu je tvořen společnými strategickými cíli, jimiž se řídí
činnost EU v oblasti kultury a které jsou stanoveny v programech Komise pro kulturu,
přičemž tento rámec je rozpracován v pracovních plánech pro kulturu sestavovaných
Radou. Tento rámec je složitý. Existuje souběžně jako součást několika obecných
strategických rámců a cílů EU, jejichž časová období a různé vrstvy odpovědnosti se
překrývají. Strategické cíle vymezené Komisí nejsou převáděny do podoby jasných
operačních cílů a neexistují žádná ustanovení, která by zajišťovala sledování plnění
stanovených cílů ani ukazatelů pro měření pokroku. Podle Komise je přenesení cílů do
tvorby politik na úrovni členských států i nadále výzvou (body 17–22).
Základní strategický rámec pro kulturu se ve financování EU odráží pouze
částečně. Kulturní aspekty jsou zapracovány nebo „začleňovány“ do různých politik
a jsou považovány hlavně za určitý zdroj, který pomáhá dosahovat jiných priorit a cílů
EU s využitím různých fondů EU. Z 12 námi analyzovaných fondů EU, které umožňují
podporovat kulturu, odkazuje na program Komise z roku 2007 pouze nařízení
„Kreativní Evropa“, což je z rozpočtového hlediska malý program. O užitečnosti
programu Komise z roku 2007 lze mít proto určité pochybnosti (body 23–24).
Komise vyvíjí řadu iniciativ zaměřených na podporu kulturních lokalit, ale kulturní
iniciativy EU mají velmi omezený dopad na dostupnost finančních prostředků EU pro
příjemce. Nařízení o EFRR stanoví vyšší horní hranice financování pro památky
UNESCO, ale pro kulturní lokality, které získaly některé z označení EU nebo které jsou
zapojeny do některé z kulturních iniciativ EU, žádná taková ustanovení neexistují.
Velmi omezená je také koordinace investic do kulturních lokalit mezi samotnými fondy
EU (body 25–36).
Na úrovni EU jsou investice do infrastruktury financovány hlavně prostřednictvím
EFRR. EFRR je zhruba pro jednu třetinu členských států významným zdrojem
financování investic do kulturních lokalit. Investice do kulturních památek však nejsou
považovány za prioritu EFRR, protože EFRR podporuje jiný cíl Smlouvy, a to posilování
sociální a hospodářské soudržnosti. Na vnitrostátní úrovni jsme našli příklady iniciativ,
jejichž prostřednictvím členské státy financují kulturní lokality a vyvíjejí systémy
financování založené na soukromých fondech (body 37–51).
Doporučení 1– Zlepšit současný strategický rámec pro kulturu
v oblasti působnosti Smluv
Komise by měla s ohledem na své pravomoci navrhnout členským státům stanovit
v dalším pracovním plánu pro kulturu jasné strategické a provozní cíle. Plnění
těchto cílů by mělo být pravidelně monitorováno prostřednictvím ukazatelů
zahrnujících dílčí cíle a milníky.
V rámci Komise by měla být jasně rozdělena a vymezena odpovědnost za plnění
těchto cílů a měla být zajištěna odpovídající úroveň koordinace.
Komise by měla určit a zainteresovaným stranám poskytnout osvědčené postupy
k tomu, jak koncipovat, vybrat, financovat, zavést a monitorovat/sledovat kulturní
projekty financované EU. To by mohlo zahrnovat zejména nastavení plánů údržby,
vývoj sociálních činností a zapojení kulturních památek do iniciativ EU.
Časový rámec: do prosince 2022.
